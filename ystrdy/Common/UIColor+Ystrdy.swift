import Foundation
import UIKit


extension UIColor {
    
    static func ystrdyDarkPurple() -> UIColor {
        return UIColor(red:0.21, green:0.18, blue:0.30, alpha:1.0)
    }
    
    static func ystrdyWarm() -> UIColor {
        return UIColor(red:0.77, green:0.31, blue:0.31, alpha:1.0)
    }
    
    static func ystrdyCool() -> UIColor {
        return UIColor(red:0.08, green:0.52, blue:0.94, alpha:1.0)
    }
}

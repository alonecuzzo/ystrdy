import UIKit

extension UIColor {
    
    class func ystrdyCold() -> UIColor {
        return UIColor(red: 0.027, green:0.192, blue: 0.255, alpha: 1.0) /* #073141 */
    }

    class func ystrdyCool() -> UIColor {
        return UIColor(red: 0.0, green:0.576, blue: 0.569, alpha: 1.0) /* #009391 */
    }

    class func ystrdyIdentical() -> UIColor {
        return UIColor(red: 0.2, green:0.835, blue: 0.549, alpha: 1.0) /* #33d58c */
    }

    class func ystrdyWarm() -> UIColor {
        return UIColor(red: 1.0, green:0.78, blue: 0.239, alpha: 1.0) /* #ffc73d */
    }

    class func ystrdyHot() -> UIColor {
        return UIColor(red: 1.0, green:0.149, blue: 0.22, alpha: 1.0) /* #ff2638 */
    }

}
